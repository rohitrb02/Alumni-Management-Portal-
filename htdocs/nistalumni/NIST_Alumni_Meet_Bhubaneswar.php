					  <h4 class="media-heading text-uppercase font-weight-500 mt-20">NIST Alumni Meet at Bhubaneswar</h4>
                    <p align="justify">NIST organized its Alumni Meet at Suryansh Hotel, Bhubaneswar, Odisha on 25th August 2019. Over 100 successful alumni settled at different parts of India and abroad participated in the meet and re-lived their memories. The event was presided by NIST Chairman, Dr Sukant Kumar Mohapatra. Other office-bearers who graced the event were Registrar, Shri Kshirod Chandra Mohapatra.</p>
                    <p align="justify"> Addressing the alumni, Chairman Dr Mohapatra said, “You’re the ambassadors and true spokespersons of the institute. Having spent the most productive and youthful years of your life in the institute, you have the common umbilical cord of the alma mater. The institute will provide platform and support for all on-campus activities of alumni association, and personal and group visit to campus.” He assured the gathering of establishing an Alumni Reception Cell to facilitate personalized and pleasurable visit to campus by NIST alumni in coordination with NIST alumni relation and outreach team lead. Alumni visit program will include: a) addressing/sharing their view with fellow students, b) visiting/meeting specific/group of faculty as they desire, c) thought sharing in specific technology/innovation/research area of interest, d) specific collaborative project with institute, and e) any specific doable activities they would like to have. NIST has a website for its alumni: alumni.nist.edu to harness the power of its alumni network. The institute plans to institutionalize "Distinguished Alumni Award" for extraordinary contributions and life time achievements of its alumni. Chairman also showed his interest to hold “25th Home Coming” with 1-2 days of dedicated event on-campus starting from 2021 for 1st batch of 1996 NIST alumni. Then after it will continue with successive batches. The event may be combined or preceded/succeeded with NIST Annual Day event, preferable during Dec-Jan every year. He told the gathering that the institute will have its alumni chapters in different parts of India and abroad.

Speaking on the event, Registrar, Shri Kshirod Mohapatra assured the alumni, “Rest assured there’ll be no problem for you for any work as long as you are in the campus. Feel at home as soon as you touch the base of the campus.”</p>
